void	CAssert::Message( const char * fmt, ... )
{
	bool	quit( false );

	pspDebugScreenPrintf( fmt );
	pspDebugScreenPrintf( "\nPress TRIANGLE to continue\n" );

	while ( quit == false )
	{
		CInput::Process();

		if ( CInput::IsButtonClicked( CInput::TRIANGLE ) == true )
		{
			quit = true;
		}

		CGfx::SwapBuffers();
	}
}

