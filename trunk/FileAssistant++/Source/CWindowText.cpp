//*************************************************************************************
//	
//*************************************************************************************
CWindowText::CWindowText( const CString & text )
:	m_Color( 0xffffffff )
,	m_szText( text )
{
	m_Color = CWindowItem::GetColor();
}

//*************************************************************************************
//	
//*************************************************************************************
void	CWindowText::Render()
{
	CFont::GetDefaultFont()->Print( m_szText, GetScreenPos(), m_Color );
}

//*************************************************************************************
//	
//*************************************************************************************
const CString &	CWindowText::GetText() const
{
	return m_szText;
}

//*************************************************************************************
//	
//*************************************************************************************
void	CWindowText::SetText( const CString & text )
{
	m_szText = text;

	SetSize( CFont::GetDefaultFont()->GetStringSize( m_szText ) );
}

//**********************************************************************************
//	
//**********************************************************************************
ARGB	CWindowText::GetColor() const
{
	return m_Color;
}

//**********************************************************************************
//	
//**********************************************************************************
void	CWindowText::SetColor( ARGB color )
{
	m_Color = color;
}

